package Shopping_basket;

public class Shopping_basket {
	int oder_id;
	
	String id;
	String cosmetic_ID;
	String cosmetic_color_ID;
	String cosmetic_num;
	
	public int getOder_id() {
		return oder_id;
	}
	public void setOder_id(int oder_id) {
		this.oder_id = oder_id;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCosmetic_ID() {
		return cosmetic_ID;
	}
	public void setCosmetic_ID(String cosmetic_ID) {
		this.cosmetic_ID = cosmetic_ID;
	}
	public String getCosmetic_color_ID() {
		return cosmetic_color_ID;
	}
	public void setCosmetic_color_ID(String cosmetic_color_ID) {
		this.cosmetic_color_ID = cosmetic_color_ID;
	}
	public String getCosmetic_count() {
		return cosmetic_num;
	}
	public void setCosmetic_count(String cosmetic_count) {
		this.cosmetic_num = cosmetic_count;
	}
	
	
	
}
